template<class T>
T sqr(T x) { return x * x; }

struct prog {

    void run() {
        ll n;
        cin >> n;
        multiset<ll> limits, nums;
        vector<ll> ans;
        for (int i = 0; i < n; ++i) {
            ll a;
            cin >> a;
            nums.insert(a);
        }
        for (int i = 0; i < n; ++i) {
            ll a;
            cin >> a;
            limits.insert(a);
        }

        for(auto it = limits.rbegin(); it!=limits.rend(); ++it){
            auto nxt = nums.upper_bound(*it);
            if(nxt==nums.begin()){
                break;
            } else{
                --nxt;
                ans.push_back(*nxt);
                nums.erase(nxt);
            }
        }
        cout << ans.size() << endl;
        for(ll a : nums){
            ans.push_back(a);
        }
        reverse(ans.begin(), ans.end());
        for(ll a : ans){
            cout << a << " " ;
        }
        cout << endl;
        for(ll a : limits)
            cout << a << " ";

    }
};


